-- Официальные тарифы Казпочты: зона × вес × тип перевозки (Авиа/Наземная).
-- Заменяет плоскую (без учёта веса) схему V47 на весовые пороги, как в официальной тарифной
-- таблице. Таблица пересоздаётся с нуля — на момент этой миграции в ней 0 строк (зоны стран ещё
-- не назначены, /delivery/intl-quote отвечал «тарифы не заданы» для всех стран), поэтому это
-- безопасно и не теряет данные.
DROP TABLE intl_zone_tariffs;

CREATE TABLE intl_zone_tariffs (
    id           BIGSERIAL PRIMARY KEY,
    zone         VARCHAR(20)   NOT NULL,
    kind         VARCHAR(10)   NOT NULL CHECK (kind IN ('AIR', 'GROUND')),
    -- Верхняя граница весового порога (кг), включительно. Для строки-надбавки
    -- (is_increment = true) это поле не используется как порог — хранится сентинел 999.000,
    -- реальное значение никогда не участвует в сравнении весов.
    upto_kg      NUMERIC(7,3)  NOT NULL,
    price_kzt    NUMERIC(12,2) NOT NULL,
    -- TRUE: price_kzt — это надбавка за КАЖДЫЙ дополнительный кг сверх максимального порога
    -- (строка «+1 кг» из тарифной таблицы), а не цена за конкретный вес.
    is_increment BOOLEAN       NOT NULL DEFAULT FALSE,
    CONSTRAINT uq_intl_zone_tariffs UNIQUE (zone, kind, upto_kg)
);

