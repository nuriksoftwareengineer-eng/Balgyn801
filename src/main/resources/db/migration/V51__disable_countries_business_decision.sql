-- Отключить международную доставку для конкретных стран (решение бизнеса,
-- не техническая проблема). active=false, а не удаление строки: сохраняет
-- уже импортированные тарифные зоны и обратимо, если решение изменится.
--
-- active=false отсекает страну и от публичного списка (/catalog/countries,
-- "Другие страны"), и от прямого запроса цены (/delivery/intl-quote --
-- CountryService.requireActiveByIso2 отклоняет неактивные страны), так что
-- это не просто визуальное скрытие в выпадающем списке.
UPDATE countries SET active = false WHERE iso2 IN (
    'AF', -- Афганистан
    'BH', -- Бахрейн
    'BN', -- Бруней
    'BT', -- Бутан
    'VE', -- Венесуэла
    'HT', -- Гаити
    'GI', -- Гибралтар
    'JO', -- Иордания
    'IQ', -- Ирак
    'IR', -- Иран
    'YE', -- Йемен
    'QA', -- Катар
    'KM', -- Коморские острова
    'KP', -- Северная Корея
    'MK', -- Северная Македония
    'AE', -- ОАЭ
    'PS', -- Палестина
    'WS', -- Самоа
    'SA', -- Саудовская Аравия
    'SY', -- Сирия
    'SO', -- Сомали
    'SD', -- Судан
    'TL'  -- Восточный Тимор (Тимор-Лешти)
);

