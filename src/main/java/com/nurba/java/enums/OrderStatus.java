package com.nurba.java.enums;

public enum OrderStatus {
    PENDING_PAYMENT, // создан, ожидает оплаты — не виден в админке, бронь склада держится до истечения окна оплаты
    NEW,            // оплачен/создан вручную — в работе у менеджера
    CONFIRMED,      // подтверждён менеджером
    IN_PRODUCTION,  // в работе — шьют/вышивают
    READY,          // готов к отправке
    SHIPPED,        // отправлен
    DELIVERED,      // доставлен клиенту
    CANCELLED,      // отменён
    EXPIRED         // оплата не поступила в срок — бронь склада освобождена, заказ закрыт (терминальный)
}
